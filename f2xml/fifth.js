// Array

let fruits = ["Apple", "Mango", "Banana", "Date"];
console.log(fruits);
console.log(fruits.length);

// Methods OF AN ARRAY
// PUSH METHOD
console.log(fruits.push("Orange"));
console.log(fruits);

// Pop Method
console.log(fruits.pop());
console.log(fruits);

// Shift Method
console.log(fruits.shift());
console.log(fruits);

// Unshift Method
console.log(fruits.unshift("Kiwi"));
console.log(fruits);

// Slice Method
console.log(fruits.slice(1,4));